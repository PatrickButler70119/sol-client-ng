<template>
  <l-control
    id = "wind-info"
    :position = "'topright'"
    v-if = 'this.wxLoaded'
  >
    <div>
      {{ hoverLatLng | positionFormat }}
    </div>
    <div>
      {{ this.wind }}
    </div>
  </l-control>
</template>

<script>
import { mapState } from 'vuex';
import { LControl } from 'vue2-leaflet';
import { radToDeg } from '../../lib/utils.js';

export default {
  name: 'WindInfo',
  components: {
    'l-control': LControl,
  },
  props: {
    hoverLatLng: {
      type: Object,
    },
  },
  filters: {
    positionFormat (value) {
      if (value === undefined) {
        return '';
      }
      return value === null ? '' : value.lat.toFixed(6) + ',' + value.lng.toFixed(6);
    },
  },
  computed: {
    wind () {
      if (this.hoverLatLng === null) {
        return '';
      }
      const wind = this.$store.getters['weather/latLngWind'](this.hoverLatLng);
      if (wind === undefined) {
        return '';
      }
      return wind.knots.toFixed(2) + 'kn @' + radToDeg(wind.twd).toFixed(2) + '\xb0';
    },
    ...mapState({
      wxLoaded: state => state.weather.loaded,
    }),
  },
}
</script>

<style scoped>
#wind-info {
  pointer-events: none;
  text-align: right;
}
</style>
